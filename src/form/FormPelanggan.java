    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
           try {
    if (tfIdPelanggan.getText().isEmpty() || tfNama.getText().isEmpty() ||
        tfAlamat.getText() .isEmpty() || tfNomorTelepon.getText().isEmpty() || 
        tfEmail.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Silahkan isi semua data terlebih dahulu!");
    } else {
        // Bagian untuk INSERT
        String sqlInsert = "INSERT INTO pelanggan (id_pelanggan, nama, alamat, nomor_telepon, email) " +
                           "VALUES (?, ?, ?, ?, ?)";
        java.sql.PreparedStatement insertpelanggan = obj.konek.prepareStatement(sqlInsert);

        insertpelanggan.setString(1, tfIdPelanggan.getText());
        insertpelanggan.setString(2, tfNama.getText());
        insertpelanggan.setString(3, tfAlamat.getText());
        insertpelanggan.setString(4, tfNomorTelepon.getText());
        insertpelanggan.setString(5, tfEmail.getText());

        // Bagian untuk UPDATE
        String sqlUpdate = "UPDATE pelanggan SET nama=?, alamat=?, nomor_telepon=?, email=? WHERE id_pelanggan=?";
        java.sql.PreparedStatement updatepelanggan = obj.konek.prepareStatement(sqlUpdate);

        updatepelanggan.setString(1, tfIdPelanggan.getText());
        updatepelanggan.setString(2, tfNama.getText());
        updatepelanggan.setString(3, tfAlamat.getText());
        updatepelanggan.setString(4, tfNomorTelepon.getText());
        updatepelanggan.setString(5, tfEmail.getText());
       

        // Eksekusi query UPDATE
        int hasilUpdate = updatepelanggan.executeUpdate();

        // Eksekusi query INSERT jika tidak ada baris yang terupdate
        if (hasilUpdate == 0) {
            int hasilInsert = insertpelanggan.executeUpdate();

            if (hasilInsert > 0) {
                tampilpelanggan();
                JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal mengubah data. Pastikan Kode yang dimasukkan benar.");
            }
        } else {
            tampilpelanggan();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
        }
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}    
    }//GEN-LAST:event_btnUbahActionPerformed

