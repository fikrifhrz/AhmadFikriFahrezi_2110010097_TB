    public void tampilpelanggan() {
   try {
            pelanggan.setRowCount(0);

            String query = "SELECT * FROM pelanggan";
            try (java.sql.PreparedStatement preparedStatement = obj.konek.prepareStatement(query);
                 ResultSet data = preparedStatement.executeQuery()) {
                while (data.next()) {
                    Object[] line = new Object[5];
                    line[0] = data.getString("id_pelanggan");
                    line[1] = data.getString("nama");
                    line[2] = data.getString("alamat");
                    line[3] = data.getString("nomor_telepon");
                    line[4] = data.getString("email");
                    pelanggan.addRow(line);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

