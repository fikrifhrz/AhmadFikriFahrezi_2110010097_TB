    public void tampiltransaksi() {
   try {
            transaksi.setRowCount(0);

            String query = "SELECT * FROM transaksi";
            try (java.sql.PreparedStatement preparedStatement = obj.konek.prepareStatement(query);
                 ResultSet data = preparedStatement.executeQuery()) {
                while (data.next()) {
                    Object[] line = new Object[8];
                    line[0] = data.getString("id_transaksi");
                    line[1] = data.getString("plat_nomor");
                    line[2] = data.getString("tanggal_sewa");
                    line[3] = data.getString("tanggal_kembali");
                    line[4] = data.getString("durasi_sewa");
                    line[5] = data.getString("harga");
                    line[6] = data.getString("total");
                    transaksi.addRow(line);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
