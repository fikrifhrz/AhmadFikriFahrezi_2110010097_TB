    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
    try {
    if (tfIdMobil.getText().isEmpty() || tfMerek.getText().isEmpty() ||
        tfModel.getText() .isEmpty() || tfTahun.getText().isEmpty() || 
        tfWarna.getText().isEmpty() || tfPlatNomor.getText().isEmpty() ||
        tfHargaSewaPerhari.getText().isEmpty() || tfStatus.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Silahkan pilih data terlebih dahulu!");
        } else {
            // Menampilkan dialog konfirmasi sebelum menghapus
            int confirmDialogResult = JOptionPane.showConfirmDialog(this,
                    "Apakah Anda yakin ingin menghapus data?", "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION);

            // Jika pengguna mengonfirmasi untuk menghapus
            if (confirmDialogResult == JOptionPane.YES_OPTION) {
                // Query SQL untuk menghapus data dari tabel
                String sql = "DELETE FROM mobil WHERE id_mobil=?";
                
                // Menggunakan try-with-resources untuk PreparedStatement
                try (java.sql.PreparedStatement hapus = obj.konek.prepareStatement(sql)) {
                    hapus.setString(1, tfIdMobil.getText());
                    
                    // Menjalankan query DELETE dan mendapatkan jumlah baris yang terpengaruh
                    int rowsAffected = hapus.executeUpdate();

                    // Jika ada baris yang terpengaruh, berarti data berhasil dihapus
                    if (rowsAffected > 0) {
                        tampilmobil();
                        JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                    } else {
                        // Jika tidak ada baris yang terpengaruh, berarti data tidak ditemukan
                        JOptionPane.showMessageDialog(this, "Gagal menghapus data. Data tidak ditemukan.");
                    }
                }
            }
        }
    } catch (SQLException | HeadlessException e) {
        // Menangani kesalahan dan menampilkan pesan kesalahan kepada pengguna
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
    try {
    if (tfIdMobil.getText().isEmpty() || tfMerek.getText().isEmpty() ||
        tfModel.getText() .isEmpty() || tfTahun.getText().isEmpty() || 
        tfWarna.getText().isEmpty() || tfPlatNomor.getText().isEmpty() ||
        tfHargaSewaPerhari.getText().isEmpty() || tfStatus.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Silahkan isi semua data terlebih dahulu!");
    } else {
        // Bagian untuk INSERT
        String sqlInsert = "INSERT INTO mobil (id_mobil, merek, model, tahun, warna, plat_nomor, harga_sewa_perhari, status) " +
                           "VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        java.sql.PreparedStatement insertmobil = obj.konek.prepareStatement(sqlInsert);

        insertmobil.setString(1, tfIdMobil.getText());
        insertmobil.setString(2, tfMerek.getText());
        insertmobil.setString(3, tfModel.getText());
        insertmobil.setString(4, tfTahun.getText());
        insertmobil.setString(5, tfWarna.getText());
        insertmobil.setString(6, tfPlatNomor.getText());
        insertmobil.setString(7, tfHargaSewaPerhari.getText());
        insertmobil.setString(8, tfStatus.getText());

        // Bagian untuk UPDATE
        String sqlUpdate = "UPDATE mobil SET merek=?, model=?, tahun=?, warna=?, plat_nomor=?, harga_sewa_perhari=?, status=? WHERE id_mobil=?";
        java.sql.PreparedStatement updatemobil = obj.konek.prepareStatement(sqlUpdate);

        updatemobil.setString(1, tfIdMobil.getText());
        updatemobil.setString(2, tfMerek.getText());
        updatemobil.setString(3, tfModel.getText());
        updatemobil.setString(4, tfTahun.getText());
        updatemobil.setString(5, tfWarna.getText());
        updatemobil.setString(6, tfPlatNomor.getText());
        updatemobil.setString(7, tfHargaSewaPerhari.getText());
        updatemobil.setString(8, tfStatus.getText());
       

        // Eksekusi query UPDATE
        int hasilUpdate = updatemobil.executeUpdate();

        // Eksekusi query INSERT jika tidak ada baris yang terupdate
        if (hasilUpdate == 0) {
            int hasilInsert = insertmobil.executeUpdate();

            if (hasilInsert > 0) {
                tampilmobil();
                JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal mengubah data. Pastikan Kode yang dimasukkan benar.");
            }
        } else {
            tampilmobil();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
        }
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}    
    }//GEN-LAST:event_btnUbahActionPerformed

