    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        // Mendapatkan baris terpilih dari tabel
              try {
    if (tfIdTransaksi.getText().isEmpty() || tfPlatNomor.getText().isEmpty() ||
        tfTanggalSewa.getText() .isEmpty() || tfTanggalKembali.getText().isEmpty() || 
        tfDurasiSewa.getText().isEmpty() || tfHarga.getText().isEmpty() || tfTotal.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Silahkan isi semua data terlebih dahulu!");
    } else {
        // Bagian untuk INSERT
        String sqlInsert = "INSERT INTO transaksi (id_transaksi, plat_nomor, tanggal_sewa, tanggal_kembali, durasi_sewa, harga, total) " +
                           "VALUES (?, ?, ?, ?, ?, ?, ?)";
        java.sql.PreparedStatement inserttransaksi = obj.konek.prepareStatement(sqlInsert);

        inserttransaksi.setString(1, tfIdTransaksi.getText());
        inserttransaksi.setString(2, tfPlatNomor.getText());
        inserttransaksi.setString(3, tfTanggalSewa.getText());
        inserttransaksi.setString(4, tfTanggalKembali.getText());
        inserttransaksi.setString(5, tfDurasiSewa.getText());
        inserttransaksi.setString(6, tfHarga.getText());
        inserttransaksi.setString(7, tfTotal.getText());

        // Bagian untuk UPDATE
        String sqlUpdate = "UPDATE transaksi SET plat_nomor=?, tanggal_sewa=?, tanggal_kembali=?, durasi_sewa=?, harga=?, total=? WHERE id_transaksi=?";
        java.sql.PreparedStatement updatetransaksi = obj.konek.prepareStatement(sqlUpdate);

        updatetransaksi.setString(1, tfIdTransaksi.getText());
        updatetransaksi.setString(2, tfPlatNomor.getText());
        updatetransaksi.setString(3, tfTanggalSewa.getText());
        updatetransaksi.setString(4, tfTanggalKembali.getText());
        updatetransaksi.setString(5, tfDurasiSewa.getText());
        updatetransaksi.setString(6, tfHarga.getText());
        updatetransaksi.setString(7, tfTotal.getText());
       
        // Eksekusi query UPDATE
        int hasilUpdate = updatetransaksi.executeUpdate();

        // Eksekusi query INSERT jika tidak ada baris yang terupdate
        if (hasilUpdate == 0) {
            int hasilInsert = inserttransaksi.executeUpdate();

            if (hasilInsert > 0) {
                tampiltransaksi();
                JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal mengubah data. Pastikan Kode yang dimasukkan benar.");
            }
        } else {
            tampiltransaksi();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
        }
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_btnUbahActionPerformed

