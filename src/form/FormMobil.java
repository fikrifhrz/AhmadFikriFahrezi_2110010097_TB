public void tampilmobil() {
   try {
            mobil.setRowCount(0);

            String query = "SELECT * FROM mobil";
            try (java.sql.PreparedStatement preparedStatement = obj.konek.prepareStatement(query);
                 ResultSet data = preparedStatement.executeQuery()) {
                while (data.next()) {
                    Object[] line = new Object[8];
                    line[0] = data.getString("id_mobil");
                    line[1] = data.getString("merek");
                    line[2] = data.getString("model");
                    line[3] = data.getString("tahun");
                    line[4] = data.getString("warna");
                    line[5] = data.getString("plat_nomor");
                    line[6] = data.getString("harga_sewa_perhari");
                    line[7] = data.getString("status");
                    mobil.addRow(line);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

